public class Day1 {

	// Learning how to use printing on console.
	
	public static void main(String[] args) {
		
		// Begin with a print w/o new line (print - println)
		System.out.print("Hello World!");
		
		/*
		 *  This is a comment
		 */
		
		System.out.print(" We have a winner! "); // Space in quot displayed
		System.out.println(); // New line - ln
		System.out.printf("%.2f", 3.14159265); // Print formatting
	}

}
